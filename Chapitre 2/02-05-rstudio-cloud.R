### Exercice 4

# Créez un nouveau document R Markdown avec le nom `sherlock-holmes.Rmd` et le format de sortie `pdf_document`.

# Ajoutez le code suivant dans l'en-tête YAML :
  
```
title: "Sherlock Holmes"
author: "Arthur Conan Doyle"
output:
  pdf_document:
  fig_caption: yes
number_sections: yes
toc: yes
toc_depth: 2
```

